
import { useState, useEffect, useMemo, useCallback } from 'react';
import { supabase } from '@/lib/supabase';
import { useAuth } from '@/context/AuthContext';
import {
  filterByPeriod,
  estimateTax,
  // Single source of truth: entry-based functions only
  buildTrialBalance,
  buildBalanceSheetFromEntries,
  buildIncomeStatementFromEntries,
  buildGeneralLedger,
  buildJournalBook,
  calculateRevenueFromEntries,
  calculateExpensesFromEntries,
  calculateNetIncomeFromEntries,
  calculateOutputVATFromEntries,
  calculateInputVATFromEntries,
  calculateVATBreakdownFromEntries,
  buildMonthlyChartDataFromEntries,
  validateAccountingConsistency,
} from '@/utils/accountingCalculations';
import { buildFinancialDiagnostic } from '@/utils/financialAnalysisCalculations';

export const useAccountingData = (startDate, endDate) => {
  const { user } = useAuth();
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  // Raw data
  const [invoices, setInvoices] = useState([]);
  const [expenses, setExpenses] = useState([]);
  const [supplierInvoices, setSupplierInvoices] = useState([]);
  const [accounts, setAccounts] = useState([]);
  const [mappings, setMappings] = useState([]);
  const [taxRates, setTaxRates] = useState([]);
  const [entries, setEntries] = useState([]);

  const fetchAll = useCallback(async () => {
    if (!user || !supabase) {
      setLoading(false);
      return;
    }

    try {
      setLoading(true);
      setError(null);

      const [invRes, expRes, supRes, accRes, mapRes, taxRes, entRes] = await Promise.all([
        supabase.from('invoices').select('*').order('date', { ascending: false }),
        supabase.from('expenses').select('*').order('date', { ascending: false }),
        (async () => { try { return await supabase.from('supplier_invoices').select('*').order('created_at', { ascending: false }); } catch { return { data: [], error: null }; } })(),
        supabase.from('accounting_chart_of_accounts').select('*').order('account_code', { ascending: true }),
        supabase.from('accounting_mappings').select('*'),
        supabase.from('accounting_tax_rates').select('*'),
        // Fetch accounting entries
        supabase.from('accounting_entries').select('*').order('transaction_date', { ascending: false }),
      ]);

      setInvoices(invRes.data || []);
      setExpenses(expRes.data || []);
      setSupplierInvoices(supRes?.data || []);
      setAccounts(accRes.data || []);
      setMappings(mapRes.data || []);
      setTaxRates(taxRes.data || []);
      setEntries(entRes.data || []);
    } catch (err) {
      console.error('Error fetching accounting data:', err);
      setError(err.message);
    } finally {
      setLoading(false);
    }
  }, [user]);

  useEffect(() => {
    fetchAll();
  }, [fetchAll]);

  // Real-time subscriptions for automatic updates with debouncing
  useEffect(() => {
    if (!user || !supabase) return;

    let refreshTimeout = null;

    // Debounced refresh to avoid multiple rapid updates
    const debouncedRefresh = () => {
      if (refreshTimeout) clearTimeout(refreshTimeout);
      refreshTimeout = setTimeout(() => {
        fetchAll();
      }, 500); // Wait 500ms after last change before refreshing
    };

    // Subscribe to accounting entries changes (auto-generated by triggers)
    const entriesSubscription = supabase
      .channel('accounting_entries_changes')
      .on('postgres_changes',
        { event: '*', schema: 'public', table: 'accounting_entries', filter: `user_id=eq.${user.id}` },
        (payload) => {
          console.log('Accounting entry changed:', payload.eventType);
          debouncedRefresh();
        }
      )
      .subscribe();

    // Subscribe to invoices changes
    const invoicesSubscription = supabase
      .channel('invoices_changes')
      .on('postgres_changes',
        { event: '*', schema: 'public', table: 'invoices', filter: `user_id=eq.${user.id}` },
        (payload) => {
          console.log('Invoice changed:', payload.eventType);
          debouncedRefresh();
        }
      )
      .subscribe();

    // Subscribe to expenses changes
    const expensesSubscription = supabase
      .channel('expenses_changes')
      .on('postgres_changes',
        { event: '*', schema: 'public', table: 'expenses', filter: `user_id=eq.${user.id}` },
        (payload) => {
          console.log('Expense changed:', payload.eventType);
          debouncedRefresh();
        }
      )
      .subscribe();

    // Subscribe to payments changes
    const paymentsSubscription = supabase
      .channel('payments_changes')
      .on('postgres_changes',
        { event: '*', schema: 'public', table: 'payments', filter: `user_id=eq.${user.id}` },
        (payload) => {
          console.log('Payment changed:', payload.eventType);
          debouncedRefresh();
        }
      )
      .subscribe();

    // Cleanup subscriptions and timeout on unmount
    return () => {
      if (refreshTimeout) clearTimeout(refreshTimeout);
      entriesSubscription.unsubscribe();
      invoicesSubscription.unsubscribe();
      expensesSubscription.unsubscribe();
      paymentsSubscription.unsubscribe();
    };
  }, [user, fetchAll]);

  // Determine if we have auto-generated entries
  const hasAutoEntries = useMemo(() => {
    return entries.some(e => e.is_auto === true);
  }, [entries]);

  // Computed values — SINGLE SOURCE OF TRUTH: all calculations from accounting_entries
  const computed = useMemo(() => {
    if (!startDate || !endDate) return null;

    // All KPIs from entries (returns 0 when no entries — clean state, no estimations)
    const revenue = calculateRevenueFromEntries(entries, accounts, startDate, endDate);
    const revenueTTC = revenue;
    const totalExpenses = calculateExpensesFromEntries(entries, accounts, startDate, endDate);
    const netIncome = calculateNetIncomeFromEntries(entries, accounts, startDate, endDate);

    const outputVAT = calculateOutputVATFromEntries(entries, accounts, startDate, endDate);
    const inputVAT = calculateInputVATFromEntries(entries, accounts, startDate, endDate);
    const vatPayable = outputVAT - inputVAT;
    const vatBreakdown = calculateVATBreakdownFromEntries(entries, accounts, startDate, endDate);

    const taxEstimate = estimateTax(netIncome > 0 ? netIncome : 0);
    const monthlyData = buildMonthlyChartDataFromEntries(entries, accounts, startDate, endDate);

    // Balance sheet & income statement — always entry-based
    const balanceSheet = buildBalanceSheetFromEntries(accounts, entries, startDate, endDate);
    const incomeStatement = buildIncomeStatementFromEntries(accounts, entries, startDate, endDate);

    // Trial balance, ledger, journal — ALL filtered by period
    const filteredEntries = filterByPeriod(entries, startDate, endDate, 'transaction_date');
    const trialBalance = buildTrialBalance(filteredEntries, accounts);
    const generalLedger = buildGeneralLedger(entries, accounts, startDate, endDate);
    const journalBook = buildJournalBook(entries, startDate, endDate);

    // Financial diagnostic
    const financialDiagnostic = buildFinancialDiagnostic(
      entries,
      accounts,
      balanceSheet,
      incomeStatement,
      startDate,
      endDate,
      null
    );

    // Consistency validation
    const consistencyWarnings = validateAccountingConsistency(
      { revenue, totalExpenses, netIncome },
      incomeStatement
    );
    if (consistencyWarnings.length > 0) {
      console.warn('Accounting consistency warnings:', consistencyWarnings);
    }

    return {
      revenue,
      revenueTTC,
      totalExpenses,
      netIncome,
      outputVAT,
      inputVAT,
      vatPayable,
      vatBreakdown,
      balanceSheet,
      incomeStatement,
      taxEstimate,
      monthlyData,
      trialBalance,
      generalLedger,
      journalBook,
      financialDiagnostic,
      consistencyWarnings,
    };
  }, [accounts, entries, startDate, endDate]);

  return {
    loading,
    error,
    // Raw data
    invoices,
    expenses,
    supplierInvoices,
    accounts,
    mappings,
    taxRates,
    entries,
    hasAutoEntries,
    // Computed values
    ...(computed || {
      revenue: 0,
      revenueTTC: 0,
      totalExpenses: 0,
      netIncome: 0,
      outputVAT: 0,
      inputVAT: 0,
      vatPayable: 0,
      vatBreakdown: { output: [], input: [] },
      balanceSheet: { assets: [], liabilities: [], equity: [], totalAssets: 0, totalLiabilities: 0, totalEquity: 0, balanced: true },
      incomeStatement: { revenueItems: [], expenseItems: [], totalRevenue: 0, totalExpenses: 0, netIncome: 0 },
      taxEstimate: { totalTax: 0, effectiveRate: 0, details: [], quarterlyPayment: 0 },
      monthlyData: [],
      trialBalance: [],
      generalLedger: [],
      journalBook: [],
      financialDiagnostic: null,
      consistencyWarnings: [],
    }),
    refresh: fetchAll
  };
};
